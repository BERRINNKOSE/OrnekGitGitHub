public class Country {
    private String code;
    private String name;
    private String continent;
    private String region;
    private String isim;

    public Country(String code, String name, String continent, String region, String isim) {
        this.code = code;
        this.name = name;
        this.continent = continent;
        this.region = region;
        this.isim = isim;
    }
}
